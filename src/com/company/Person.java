package com.company;

public abstract class Person {
    String name;

    public Person() {
        name = "";
    }
    public Person(String name) {
        name = "";
    }
    //Getters and setters
    public void setName(String n) {
        name = n;
    }
    //Getters and setters
    public String getName() {
        return name;
    }

}
